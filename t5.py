'''
Операції з числами
Операції: + - * /
Створіть дві змінні зі значеннями 785 і 157.
Надрукуте по порядку результати наступних дій з ними:
сума,
різниця (перше мінус друге),
множення,
ділення (перше розділити на друге).
'''

a = 785
b = 157
sum_result = a + b
difference_result = a - b
multiplication_result = a * b
division_result = a / b
print("Сума:", sum_result)
print("Різниця:", difference_result)
print("Множення:", multiplication_result)
print("Ділення:", division_result)
